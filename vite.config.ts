import { defineConfig } from 'vite'
import react from '@vitejs/plugin-react'

const repoName =
  process.env.GITHUB_REPOSITORY?.split('/')[1] ?? 'ripplehabits'

export default defineConfig(({ mode }) => ({
  plugins: [react()],
  base:
    process.env.GITHUB_ACTIONS === 'true' || mode === 'production'
      ? `/${repoName}/`
      : '/',
}))
